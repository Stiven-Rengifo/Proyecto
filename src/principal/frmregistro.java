    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String cc=txtcc.getText();
        String password=txtpassword.getText();
        String tipousuario=combotipo.getSelectedItem().toString();
        
        if (nombre.isEmpty()||apellido.isEmpty()||cc.isEmpty()||password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe completar todos los datos");
        }else{
            if(tipousuario.equalsIgnoreCase("Seleccione")){
                JOptionPane.showMessageDialog(null, "Debe seleccionar tipo de usuario");
            }else{
                try {
                    String consulta="INSERT INTO `usuario`(`Nombre`, `Apellido`, `CC`, `Tipo usuario`, `Password`) VALUES ('"+nombre+"','"+apellido+"','"+cc+"','"+tipousuario+"','"+password+"')";
                    PreparedStatement ps = (PreparedStatement) cn.prepareStatement(consulta);
                    ps.executeUpdate();
                    limpiar();
                    JOptionPane.showMessageDialog(null, "Datos guardados correctamente");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "No se pudo guardar usuario"+e);
                    
                }
                        
                
            }
        }
           
    }//GEN-LAST:event_jButton1ActionPerformed
        void limpiar(){
            txtnombre.setText("");
            txtapellido.setText("");
            txtcc.setText("");
            txtpassword.setText("");
        }
